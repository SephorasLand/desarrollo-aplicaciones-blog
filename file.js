function showMore1(){
    document.getElementById("entrada1").style.display = "block";
    document.getElementById("enlaceEntrada1mas").style.display = "none";
    document.getElementById("enlaceEntrada1menos").style.display = "block";
}

function showLess1(){
    document.getElementById("entrada1").style.display = "none";
    document.getElementById("enlaceEntrada1mas").style.display = "block";
    document.getElementById("enlaceEntrada1menos").style.display = "none";
}

